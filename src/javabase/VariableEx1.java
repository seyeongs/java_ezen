package javabase;

public class VariableEx1 {

	public static void main(String[] args) {
		int num16 = 0x36A; // 16진수 표현식 0x
		int num8 = 036; //8진수 표현식 0
		
		int num1 = 10; //10진수
		int num2 = 2;
		
		System.out.println(num1);
		System.out.println(num1+num2);
		System.out.println(num1-num2);
		System.out.println(num1*num2);
		System.out.println(num1/num2);
		
	}

}
